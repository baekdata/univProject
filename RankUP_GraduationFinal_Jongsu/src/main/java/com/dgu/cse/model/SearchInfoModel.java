package com.dgu.cse.model;

public class SearchInfoModel {
	private int id;
	private String _link;
	private int _category;
	private String _blogName;
	private int _postId;
	private String _description;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String get_link() {
		return _link;
	}
	public void set_link(String _link) {
		this._link = _link;
	}
	public int get_category() {
		return _category;
	}
	public void set_category(int _category) {
		this._category = _category;
	}
	public String get_blogName() {
		return _blogName;
	}
	public void set_blogName(String _blogName) {
		this._blogName = _blogName;
	}
	public int get_postId() {
		return _postId;
	}
	public void set_postId(int _postId) {
		this._postId = _postId;
	}
	public String get_description() {
		return _description;
	}
	public void set_description(String _description) {
		this._description = _description;
	}
	
	
}
